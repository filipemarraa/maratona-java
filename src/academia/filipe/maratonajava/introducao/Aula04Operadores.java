package academia.filipe.maratonajava.introducao;

public class Aula04Operadores {
    public static void main(String[] args) {
        int number1 = 20;
        int number2 = 10;
        //Operadores matematicos + | - | / |*
        System.out.println(number1 + number2); // 20 + 10 = 30
        System.out.println(number1 - number2); // 20 - 10 = 10
        System.out.println(number1 / number2); // 20 / 10 = 2
        System.out.println(number1 * number2); // 20 * 10 = 200
    }
}
